char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			if (str[i - 1] < 48)
			{
				str[i] = str[i] - 'a' + 'A';
			}
		}
		i++;
	}
	return (str);
}
